<?php

// tipe data integer
    echo "Decimal : ";
    var_dump(1234);

    echo "Octal : ";
    var_dump(0123);

    echo "Hexadecimal : ";
    var_dump(0x1a);

    echo "Binary : ";
    var_dump(0b111111);

    echo "Underscore in number : ";
    var_dump(1_212_312_421);

// end tipe data integer

// tipe data float
    echo "floating point : ";
    var_dump(1.234);

    echo "floating point dengan E Notation : (1.7 x 1000) ";
    var_dump(1.7e3);

    echo "floating point dengan E Notation minus : (1.7 x 0.001) ";
    var_dump(1.7e-3);

    echo "Underscore di floating point : ";
    var_dump(1_234.123);

    echo "Integer overflow 32 bit : ";
    var_dump(1234435698);

    echo "Integer overflow 64 bit : ";
    var_dump(9884435698128088777);
// end tipe data float