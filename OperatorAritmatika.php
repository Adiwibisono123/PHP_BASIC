<?php

// contoh pertama
$a = 10;
$b = 10;

// contoh kedua
$result = 10 + 10;

// operator pengurangan
$result = $a - $b;

// operator pertambahan
$result = $a + $b;
var_dump($result);

// operator perkalian
$jumlah  = 5 * 5;
var_dump($jumlah);

// operator pembagian
$c = 25;
$d = 5;
$nilai = $c / $d;

var_dump($nilai);

// operator sisa bagi
$y = 20;
$x = 3;

$sisa = $y % $x;
var_dump($sisa);

// operator pangkat
$v = 100;
$i = 2;

$pangkat = $v ** $i;
var_dump($pangkat);

// nilainya diubah menjadi negatif
$resultNegative = -$result;
var_dump($resultNegative);

// nilainya diubah dari negatif mennjadi positif
$resultPositif = +$result;
var_dump($resultPositif);